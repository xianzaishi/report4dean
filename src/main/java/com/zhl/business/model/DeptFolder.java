package com.zhl.business.model;

/**
 * 单位文件部门实体类
 * 
 * @author 张宏亮
 * 
 */
public class DeptFolder {
	/**
	 * id
	 */
	private int id;

	/**
	 * 部门名称
	 */
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
